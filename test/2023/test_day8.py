import inspect

from aoc._2023.day8 import part1, part2


def test_part1():
    data = inspect.cleandoc(
        """
        RL

        AAA = (BBB, CCC)
        BBB = (DDD, EEE)
        CCC = (ZZZ, GGG)
        DDD = (DDD, DDD)
        EEE = (EEE, EEE)
        GGG = (GGG, GGG)
        ZZZ = (ZZZ, ZZZ)
    """
    ).splitlines()
    assert part1(data) == 2


def test_part2():
    data = inspect.cleandoc(
        """
        LR

        11A = (11B, XXX)
        11B = (XXX, 11Z)
        11Z = (11B, XXX)
        22A = (22B, XXX)
        22B = (22C, 22C)
        22C = (22Z, 22Z)
        22Z = (22B, 22B)
        XXX = (XXX, XXX)
    """
    ).splitlines()
    assert part2(data) == 6
